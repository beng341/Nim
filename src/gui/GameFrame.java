    @Override
    public void paint(Graphics g) {
        super.paint(g);
    
        for (int i = 1; i <= piles.size(); i++) {
            g.setColor(Color.BLUE);
            g.fillRect(i * 60, 200, 90,60);
            g.setColor(Color.red);
            g.drawString(piles.get(i).toString(), i * 60, 200);
        }
    }
    
    
