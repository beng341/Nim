    public GameFrame(ArrayList piles) {
        
        this.setVisible(true);
        this.setSize(800, 600);
        this.setAlwaysOnTop(false);
        this.setDefaultCloseOperation(JFrame.EXIT_ON_CLOSE);
      
        this.piles = piles;
        
        this.validate();
        
        update();
    }

